# Swapping Two Numbbers
# Example Input: 
    # Enter The First Number  : 10
    # Enter The Second Number : 20

#source code ~

a=int(input("Enter The First Number  :"))
b=int(input("Enter The Second Number :"))

temp = a
a = b
b = temp
print("Your Swapped First Number  ↓↑  :",a)
print("Your Swapped Second Number ↓↑  :",b)


#Output:
# Your Swapped First Number  ↓↑  : 20
# Your Swapped Second Number ↓↑  : 10
